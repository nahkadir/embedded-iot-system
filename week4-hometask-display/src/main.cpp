void setup() {
  Wire.begin(21, 22); // ESP32 default I2C pins (SDA=21, SCL=22)

  if (!display.begin(SSD1306_SWITCHCAPVCC, OLED_ADDR)) {
    // If it fails, check wiring and address (0x3C/0x3D)
    for (;;);
  }

  display.clearDisplay();
  
}

void loop() {
 
  display.clearDisplay();

  display.drawBitmap(25, 5, bitmap_star, 64, 64, SSD1306_WHITE);

  display.display();
  delay(3000);

  display.clearDisplay();

  display.setTextSize(2);
  display.setTextColor(SSD1306_WHITE);
  display.setCursor(10, 26);
  display.println("Rida 1087");
  display.drawRect(7, 22, 110, 20, SSD1306_WHITE);
  display.display();
  delay(2000);
}